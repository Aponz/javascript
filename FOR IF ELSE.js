var jmlAngkot = 10;
var noAngkot = 1;
var angkotBisa = 6;
for (noAngkot; noAngkot <= jmlAngkot; noAngkot++) {
    if (noAngkot <= angkotBisa) {
        console.log('Angkot no ' + noAngkot + ' beroperasi dengan baik ');
    } else {
        console.log('Angkot no ' + noAngkot + ' sedang tidak beroperasi');
    }
}